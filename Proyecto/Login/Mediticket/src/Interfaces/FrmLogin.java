    public void cargarCombo(){
        for (int i = 0; i < rolRegistrado.getRolCount(); i++) {
            Rol rol = new Rol();
            rol = rolRegistrado.findRol(i+1);
            cmbtipo.insertItemAt(rol.getNombrerol(),i);
        }
    }
