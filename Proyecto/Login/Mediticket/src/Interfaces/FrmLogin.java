    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
        // TODO add your handling code here:
        String direccionIP = txtdireccionIP.getText();
            conexion = new Conexiones(direccionIP);
            //ConnectionProperties conexion = new ConnectionProperties(txtIP.getText());
            
            if(!"".equals(direccionIP)){
                conexion.getConexion();
                if(conexion.isIngreso() == true){
                    bloqueoIngreso(true);
                    txtdireccionIP.setEnabled(false);
                    btnVerificar.setEnabled(false);
                    usuarioRegistrado = new UsuarioJpaController(direccionIP);
                    rolRegistrado = new RolJpaController(direccionIP);
                    rolUsuariRegistrado = new RolusuarioJpaController(direccionIP);
                    creacionUser();
                    cargarCombo();
                }else{
                    txtdireccionIP.setText("");
                }
            }else{
                JOptionPane.showMessageDialog(rootPane,"Ingrese una dirección del servidor","Error",JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_btnVerificarActionPerformed
